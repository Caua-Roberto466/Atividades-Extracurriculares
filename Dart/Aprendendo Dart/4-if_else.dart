    print('O número 1 é maior que o número 2');
  } else {
    print('O número 1 é menor que o número 2');
  }

  // Caso if e else
  if (numero1 == numero2) {
    print('O número 1 é igual ao número 2');
  } else {
    print('O número 1 é diferente do número 2');
  }

  //Caso if solto

  if (numero1 == numero2) {
    print('numero1 é igual a numero2');
  }

  if (numero1 > numero2) {
